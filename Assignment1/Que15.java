public class Que15 
{
    public static void main(String[] args) 
	{
        int a = 10;
        int b = 20;
        int temp =0;
        System.out.println("Variable before Swaping: a = "+a+" b = "+b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("Variable after  Swaping: a = "+a+" b = "+b);
        

    }
    
}
